"""
EMBEDDING"""

from sentence_transformers import SentenceTransformer

model = SentenceTransformer('all-MiniLM-L6-v2')

columsEMB = ['title', 'description', 'related_topics', 'similar_questions']
df_limpio[columsEMB] = df_limpio[columsEMB].fillna('')

def unificarTexto(row):
    partes = []

    if pd.notna(row['title']):
        partes.append(f"TITULO: {row['title']}")

    if pd.notna(row['description']):
        partes.append(f"DESCRIPCION: {row['description']}")

    if pd.notna(row['related_topics']):
        partes.append(f"TEMAS: {row['related_topics']}")

    if pd.notna(row['similar_questions']):
        partes.append(f"PREGUNTAS SIMILARES: {row['similar_questions']}")

    return "\n".join(partes)

"""APLICAR EMBEDDING"""

df_limpio['text_for_embedding'] = df_limpio.apply(unificarTexto, axis=1)

df_limpio['text_for_embedding'] = df_limpio['text_for_embedding'].str.lower().str.strip()

embeddings = model.encode(df_limpio['text_for_embedding'].tolist(), show_progress_bar=True)


np.save("embeddings.npy", embeddings)
df_limpio.drop(columns=['text_for_embedding']).to_csv("metadata_problemas.csv", index=False)

