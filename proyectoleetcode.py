from sklearn.metrics.pairwise import cosine_similarity

def problemas_similares(
    df,
    embeddings,
    problem_id,
    top_k=5
):

    idx = df.index[df["id"] == problem_id][0]
    query_embedding = embeddings[idx].reshape(1, -1)


    similarities = cosine_similarity(query_embedding, embeddings)[0]

    df_tmp = df.copy()
    df_tmp["similarity"] = similarities

    df_tmp = df_tmp[df_tmp["id"] != problem_id]

    resultados = df_tmp.sort_values(
        "similarity",
        ascending=False
    ).head(top_k)


    resultados = resultados.sort_values("difficulty_score")

    return resultados[
        ["id", "title", "difficulty_score", "similarity"]
    ]

resultados = problemas_similares(
    df_limpio,
    embeddings,
    problem_id=int(input("Indica que ejercicio quieres 1-1825")),
    top_k=5
)


